public interface Sprite //Sprite interface that will allow us to reference to sprites and group them easily
{ //Sprite interface start

	public int getX(); //All sprites must have a getter for their x value
	
	public int getY(); //All sprites must have a getter for their y value
	
	public void setX(int x); //All sprites must have a setter for their x value
	
	public void setY(int y); //All sprites must have a setter for their y value
	
} //Sprite interface end